
public class Discount {
	public static void main(String args[]) {
		int marketprice=1000;
		 int dis=25;
		System.out.println("mark"+marketprice);
		System.out.println("dis"+dis);
		 int s=100-dis;
		int amount=(s*marketprice)/100;
		System.out.println("discount"+amount);
	}
		
	

}
